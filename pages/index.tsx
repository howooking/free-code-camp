import Head from 'next/head';
import Image from 'next/image';
import Link from 'next/link';

export async function getServerSideProps() {
  const data = await import('../data/data.json');
  const cities = data.events_categories;

  return {
    props: {
      cities,
    },
  };
}

const header = (
  <header>
    <nav>
      <Link href="/">Home</Link>
      <Link href="/about">About</Link>
      <Link href="/events">Events</Link>
    </nav>
  </header>
);
const footer = <footer>footer</footer>;

interface HomeProps {
  cities: {
    id: string;
    title: string;
    description: string;
    image: string;
  }[];
}

export default function Home({ cities }: HomeProps): JSX.Element {
  return (
    <>
      <Head>
        <title>11</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      {header}
      {cities.map((city) => (
        <Link href={`/events/${city.id}`} key={city.id}>
          <div className="w-[600px] h-[300px] relative bg-purple-100">
            <Image
              src={city.image}
              alt="cityImg"
              fill
              className="object-contain"
            />
          </div>
          <h6 className="text-xl font-bold text-purple-500">{city.title}</h6>
          <p className="text-sm text-slate-600">{city.description}</p>
        </Link>
      ))}
      {footer}
    </>
  );
}
